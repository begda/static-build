<template>
  <div class="progress">
    <div :class="['progress__bar','progress--complete', statusStyle]" :style="{width:progress+'%'} ">
      <div class="progress__text"></div>
    </div>
  </div>
</template>

<script>
module.exports = {
  props: {
    value: {required: true, type: Number},
    status: {type: String, default: 'success'},
  },
  data() {
    return {
      progress: 0
    };
  },
  watch: {
    value(newData, oldData) {
      this.progress = newData
      this.$emit('change', newData)
    }
  },
  mounted() {

  },
  methods: {
    style(status) {
      switch (status) {
        case 'primary':
          return 'progress__bar--blue'
        case 'success':
          return 'progress__bar--green'
        case 'warning':
          return 'progress__bar--yellow'
        case 'danger':
          return 'progress__bar--orange'
        default:
          return 'progress__bar--blue'
      }
    }

  },
  computed: {
    //赋值不同的css 颜色
    statusStyle() {
      return this.style(this.status)
    }
  }
};
</script>

